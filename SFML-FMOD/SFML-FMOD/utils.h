class IdPool
{
public:
	IdPool(size_t idCount) {
		numIds = idCount;
		idsOccupiedList.resize(numIds, false);
	}

	int getFreeID() {
		for (int i = 0; i < numIds; i++) {
			if (idsOccupiedList[i] == false) return i;
		}
		return -1;
	}

	void setIDInUse(int id, bool inUse) {
		idsOccupiedList[id] = inUse;
	}

private:
	size_t numIds;
	std::vector<bool> idsOccupiedList;
};