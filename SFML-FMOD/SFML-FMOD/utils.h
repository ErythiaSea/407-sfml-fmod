struct RectF {
	float top, left, bottom, right = 0.0f;
	float width, height = 0.0f;

	sf::Vector2f position;
	sf::Vector2f size;
	sf::Vector2f center;

	RectF(sf::FloatRect r) {
		left = r.position.x;
		right = r.position.x + r.size.x;
		top = r.position.y;
		bottom = r.position.y + r.size.y;

		height = bottom - top;
		width = right - left;

		position = r.position;
		size = r.size;
		center = r.getCenter();
	}
};

